package Modelo;

public class Casas {
    private int idCasa;
    private int numeroCasa;
  

    public Casas() {
    }

    public Casas(int numeroCasa) {
        this.numeroCasa = numeroCasa;
   
    }

    public int getIdCasa() {
        return idCasa;
    }

    public void setIdCasa(int idCasa) {
        this.idCasa = idCasa;
    }

    public int getNumeroCasa() {
        return numeroCasa;
    }

    public void setNumeroCasa(int numeroCasa) {
        this.numeroCasa = numeroCasa;
    }



}
